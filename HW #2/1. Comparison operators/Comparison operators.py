# До операторів порівняння відносяться наступні:

# "<"      - менше 
# ">"      - більше
# "=="     - дорівнює
# ">="     - більше або дорівнює
# "<="     - менше або дорівнює
# "!="     - не дорівнює
# "not"    - не є чимось
# "is"     - є тим самим
# "is not" - не є тим самим
# "in"     - є частиною чогось
# "not in" - не є частиною чогось

x = 5
y = 10
z = 15

print((x < y) and (y <= z)) # True
print(x < y <= z) # True
